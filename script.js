//Q-8
{
    let option = 3;
    let balance = 1000;
    let amount = 200;

    console.log("1. withdrawn money");
    console.log("2. deposited money");
    console.log("3. check balance");
    console.log("select any option from 1-3");

    switch (option) {
        case 1:
            balance = balance - amount;
            console.log("withdrawn money:", amount);
            console.log("new balance:", balance);
            break;
        case 2:
            balance = balance + amount;
            console.log("deposited money:", amount);
            console.log("new balance:", balance);
            break;
        case 3:
            console.log("check balance:", balance);
            break;
        default:
            console.log("invalid option");
            break;
    }
}

